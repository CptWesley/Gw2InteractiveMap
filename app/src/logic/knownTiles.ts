export function tileIsKnown(continent: number, floor: number, zoom: number, x: number, y: number) {
    const tiles = knownTiles[continent][floor].Tiles;
    if (!tiles[zoom] || !tiles[zoom][x]) {
        return false;
    }

    return tiles[zoom][x].includes(y);
}
